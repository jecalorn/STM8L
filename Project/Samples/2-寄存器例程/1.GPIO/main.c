#include"iostm8l152k4.h"
/*******************************************************************************
****入口参数：无
****出口参数：无
****函数备注：GPIO测试程序，PC4灌流方式连接LED，通过控制CD4输出电平，使LED闪烁
****版权信息：蓝旗嵌入式系统
*******************************************************************************/
int main( void )
{
    int i,j;
    PC_DDR_DDR4=1;           //端口数据方向寄存器，输出
    PC_CR1_C14=1;            //端口控制寄存器1，推挽输出
    PC_CR2_C24=0;            //端口控制寄存器2，输出速率2MHz
    while (1)
    {
      PC_ODR_ODR4=1;  //端口输出数据寄存器，输出高电平
      for(i=0;i<100;i++)
        for(j=0;j<200;j++);
      PC_ODR_ODR4=0;  //端口输出数据寄存器，输出低电平
      for(i=0;i<100;i++)
        for(j=0;j<200;j++);
           
     
     }
    return 0;
}
